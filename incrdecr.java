import java.util.Scanner;
public class incrdecr{
public static void main(Strings[]args){
    int n;
    System.out.println("Enter the number:-");
    Scanner s= new Scanner(System.in);
    n=s.nextInt();
    System.out.println(n++);//5
    System.out.println(++n);//7
    System.out.println(n--);//7
    System.out.println(--n);//5
    System.out.println(n);//5
    

}

}